package com.zosh.modal;

// DistanceTime.java
public class DistanceTime {
    private String distance;
    private String time;

    public DistanceTime(String distance, String time) {
        this.distance = distance;
        this.time = time;
    }

    public String getDistance() {
        return distance;
    }

    public String getTime() {
        return time;
    }
}